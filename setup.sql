DROP DATABASE IF EXISTS `ratpack03`;

CREATE DATABASE `ratpack03`;

USE `ratpack03`;

CREATE TABLE `USER` (
  ID           INT PRIMARY KEY AUTO_INCREMENT,
  `EMAIL`      VARCHAR(255),
  `FIRSTNAME`  VARCHAR(255),
  `LASTNAME`   VARCHAR(255),
  `ADDRESS1`   VARCHAR(255),
  `ADDRESS2`   VARCHAR(255),
  `CITY`       VARCHAR(255),
  `STATE`      VARCHAR(255),
  DATE_CREATED DATE,
  LAST_UPDATED DATE
);

