CREATE TABLE `EVENT` (
  ID           INT PRIMARY KEY AUTO_INCREMENT,
  `TYPE`       VARCHAR(255),
  `DETAIL`     VARCHAR(255),
  DATE_CREATED DATE,
  LAST_UPDATED DATE
